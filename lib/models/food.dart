class FoodModel {
  final int price;
  final String name;
  final String imageUrl;
  final String description;
  final String category;
  final String id;
  final String time;

  FoodModel({
    required this.price,
    required this.name,
    required this.imageUrl,
    required this.description,
    required this.category,
    required this.id,
    required this.time,
  });
}
